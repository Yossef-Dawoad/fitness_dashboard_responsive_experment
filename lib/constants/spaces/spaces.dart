/// Defines a class that encapsulates various space-related constants and utilities.
/// This class is part of the 'spaces' library and is intended to be used throughout the application.
class Spaces {
  /// - `xxs`: 4 units
  static const double xxs = 4;

  /// - `xs`: 8 units
  static const double xs = 8;

  /// - `s`: 12 units
  static const double s = 12;

  /// - `m`: 16 units
  static const double m = 16;

  /// - `l`: 20 units
  static const double l = 20;

  /// - `xl`: 24 units
  static const double xl = 24;

  /// - `xxl`: 32 units
  static const double xxl = 32;

  /// - `xxxl`: 40 units
  static const double xxxl = 40;

  //-- App Spascfic
  /// - `defaultPd`: 20 units wich equals to `l`
  static const double defaultPd = s;

  /// - `btwSections`: 16 units wich equals to `m`
  static const double btwSections = m;

  /// - `btwItems`: 12 units wich equals to `s`
  static const double btwItems = s;
}
